package vo;

public class StockVo {

	int    s_idx;
	int    s_cnt;
	String stock_regdate;
	int    b_idx;
	
	public int getS_idx() {
		return s_idx;
	}
	public void setS_idx(int s_idx) {
		this.s_idx = s_idx;
	}
	public int getS_cnt() {
		return s_cnt;
	}
	public void setS_cnt(int s_cnt) {
		this.s_cnt = s_cnt;
	}
	public String getStock_regdate() {
		return stock_regdate;
	}
	public void setStock_regdate(String stock_regdate) {
		this.stock_regdate = stock_regdate;
	}
	public int getB_idx() {
		return b_idx;
	}
	public void setB_idx(int b_idx) {
		this.b_idx = b_idx;
	}
	
	
}
